    // ── 6. Multi-page Report ─────────────────────────────────────────────

    [Fact]
    public void Fluent_MultiPageReport()
    {
        var path = OutputPath("fluent-06-multipage-report.pdf");

        Pdf.Create()
            .WithTheme(PdfTheme.Modern)
            .WithMetadata(m => m.Title("Q4 2024 Report").Author("Analytics Team"))
            // Cover page
            .AddPage(p => p
                .Body(b => b
                    .Spacer().Spacer().Spacer()
                    .Rectangle(o =>
                    {
                        o.FillColor = "#2C3E50";
                        o.Width = "170mm";
                        o.Height = "3mm";
                    })
                    .Spacer()
                    .Heading1("Quarterly Report")
                    .Text("Q4 2024  |  October - December", o =>
                    {
                        o.FontSize = 16;
                        o.Color = "#888888";
                    })
                    .Spacer().Spacer()
                    .MutedText("Prepared by Analytics Team")
                    .MutedText("January 2025")))
            // Content page
            .AddPage(p => p
                .Header(h => h.MutedText("Q4 2024 Report  |  Confidential"))
                .Body(b => b
                    .Heading1("1. Revenue Performance")
                    .Line()
                    .Spacer()
                    .Paragraph(
                        "Revenue growth exceeded targets across all business units. Total Q4 revenue reached $12.8M, a 23% increase year-over-year.")
                    .Spacer()
                    .Table(t => t
                        .Column("Revenue Stream", "35%")
                        .Column("Q3", "16%", Alignments.Right)
                        .Column("Q4", "16%", Alignments.Right)
                        .Column("Change", "16%", Alignments.Right)
                        .Column("% Total", "17%", Alignments.Right)
                        .Row("SaaS Subscriptions", "$4.2M", "$5.1M", "+21%", "39.8%")
                        .Row("Enterprise Licenses", "$2.8M", "$3.4M", "+21%", "26.6%")
                        .Row("Professional Services", "$1.5M", "$2.0M", "+33%", "15.6%")
                        .Row("Support", "$1.1M", "$1.3M", "+18%", "10.2%"))
                    .Spacer()
                    .AccentText("Total Q4 Revenue: $12.8M (+23% YoY)"))
                .Footer(f => f.CaptionText("Page {pageNumber} of {pageCount}  |  Confidential")))
            // KPIs page
            .AddPage(p => p
                .Header(h => h.MutedText("Q4 2024 Report  |  Confidential"))
                .Body(b => b
                    .Heading1("2. Key Performance Indicators")
                    .Line()
                    .Spacer()
                    .Table(t => t
                        .Column("KPI", "35%")
                        .Column("Target", "16%", Alignments.Right)
                        .Column("Actual", "16%", Alignments.Right)
                        .Column("Status", "16%", Alignments.Center)
                        .Column("Trend", "17%", Alignments.Center)
                        .Row("MRR", "$4.2M", "$4.5M", "Exceeded", "Up")
                        .Row("CAC", "$1,200", "$980", "Exceeded", "Down")
                        .Row("Churn Rate", "< 3%", "2.4%", "Exceeded", "Down")
                        .Row("NPS Score", "> 70", "82", "Exceeded", "Up")
                        .Row("Uptime", "99.9%", "99.97%", "Exceeded", "Stable"))
                    .Spacer()
                    .Paragraph(
                        "All tracked KPIs exceeded targets. Customer acquisition cost dropped 18% below target due to improved marketing efficiency."))
                .Footer(f => f.CaptionText("Page {pageNumber} of {pageCount}  |  Confidential")))
            .GenerateToFile(path);

        AssertValidPdf(path);
    }

    // ── 7. Generate to byte[] ────────────────────────────────────────────

    [Fact]
    public void Fluent_GenerateToBytes()
    {
        var bytes = Pdf.Create()
            .WithTheme(PdfTheme.Default)
            .AddPage(p => p.Body(b => b.Heading1("Byte array test")))
            .Generate();

        Assert.True(bytes.Length > 100);
        Assert.Equal((byte)'%', bytes[0]);
        Assert.Equal((byte)'P', bytes[1]);
    }

    // ── 8. Generate to stream ────────────────────────────────────────────

    [Fact]
    public void Fluent_GenerateToStream()
    {
        using var stream = new MemoryStream();

        Pdf.Create()
            .WithTheme(PdfTheme.Default)
            .AddPage(p => p.Body(b => b.Text("Stream test")))
            .GenerateToStream(stream);

        Assert.True(stream.Length > 100);
        stream.Position = 0;
        Assert.Equal((byte)'%', stream.ReadByte());
        Assert.Equal((byte)'P', stream.ReadByte());
    }

