using R4PDF.Fluent;
using R4PDF.Fluent.Themes;

namespace R4PDF.Tests;

public class FluentPdfGenerationTests : IDisposable
{
    private readonly string _outputDir;

    public FluentPdfGenerationTests()
    {
        _outputDir = Path.GetFullPath(
            Path.Combine(AppContext.BaseDirectory, "..", "..", "..", "generated-pdfs"));
        Directory.CreateDirectory(_outputDir);
    }

    public void Dispose()
    {
    }

    private string OutputPath(string name)
    {
        return Path.Combine(_outputDir, name);
    }

    private void AssertValidPdf(string path)
    {
        Assert.True(File.Exists(path), $"PDF was not created at: {path}");
        var bytes = File.ReadAllBytes(path);
        Assert.True(bytes.Length > 100, "PDF file is suspiciously small");
        Assert.Equal((byte)'%', bytes[0]);
        Assert.Equal((byte)'P', bytes[1]);
        Assert.Equal((byte)'D', bytes[2]);
        Assert.Equal((byte)'F', bytes[3]);
    }

    // ── 1. Hello World (Default theme) ───────────────────────────────────

    [Fact]
    public void Fluent_HelloWorld_DefaultTheme()
    {
        var path = OutputPath("fluent-01-hello-world.pdf");

        Pdf.Create()
            .WithTheme(PdfTheme.Default)
            .WithMetadata(m => m.Title("Hello World - Fluent API"))
            .AddPage(page => page
                .Body(b => b
                    .Heading1("Hello, World!")
                    .Text("This PDF was generated using the R4PDF fluent API.")
                    .Spacer()
                    .MutedText("Generated by R4PDF Fluent Builder")))
            .GenerateToFile(path);

        AssertValidPdf(path);
    }

