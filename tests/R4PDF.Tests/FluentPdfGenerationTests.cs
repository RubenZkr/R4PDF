    // ── 4. Custom Theme ──────────────────────────────────────────────────

    [Fact]
    public void Fluent_CustomTheme()
    {
        var path = OutputPath("fluent-04-custom-theme.pdf");

        var brandTheme = new PdfThemeBuilder(PdfTheme.Default)
            .Heading1(s =>
            {
                s.Color = "#8B0000";
                s.FontSize = 26;
            })
            .Heading2(s => { s.Color = "#A52A2A"; })
            .Accent(s => { s.Color = "#FF4500"; })
            .Table(t =>
            {
                t.HeaderBackgroundColor = "#8B0000";
                t.AlternateColor = "#FFF5F5";
            })
            .Line(l => l.Color = "#8B0000")
            .Build();

        Pdf.Create()
            .WithTheme(brandTheme)
            .WithMetadata(m => m.Title("Custom Brand Theme"))
            .AddPage(page => page
                .Body(b => b
                    .Heading1("Custom Brand Theme")
                    .Heading2("Red & Professional")
                    .Spacer()
                    .Paragraph(
                        "This document uses a custom theme built on top of the Default theme. The heading colors, table header, and line colors have been overridden to match a brand palette.")
                    .Spacer()
                    .AccentText("Accent text in brand orange!")
                    .Spacer()
                    .Table(t => t
                        .Column("Department", "40%")
                        .Column("Budget", "30%", Alignments.Right)
                        .Column("Spent", "30%", Alignments.Right)
                        .Row("Engineering", "$500,000", "$480,000")
                        .Row("Marketing", "$300,000", "$320,000")
                        .Row("Operations", "$200,000", "$190,000"))
                    .Spacer()
                    .Line()
                    .MutedText("Confidential — Internal Use Only")))
            .GenerateToFile(path);

        AssertValidPdf(path);
    }

