    // ── 10. PdfGenerator.Generate(PdfTemplate) directly ──────────────────

    [Fact]
    public void PdfGenerator_AcceptsPdfTemplate()
    {
        var template = Pdf.Create()
            .WithTheme(PdfTheme.Default)
            .WithMetadata(m => m.Title("Direct Template"))
            .AddPage(p => p.Body(b => b.Heading1("Direct").Paragraph("Content")))
            .Build();

        var generator = new PdfGenerator();
        var bytes = generator.Generate(template);

        Assert.True(bytes.Length > 100);
        Assert.Equal((byte)'%', bytes[0]);
    }
